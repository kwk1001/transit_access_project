make_diverging_palette <- function(x) {
  lim <- max(abs(x), na.rm = TRUE)
  if (!is.finite(lim) || lim == 0) lim <- 1
  leaflet::colorNumeric(
    palette = colorRampPalette(c("#264653", "#2a9d8f", "#edf2fb", "#f3722c", "#f94144"))(256),
    domain = c(-lim, lim),
    na.color = "#dddddd"
  )
}

make_sequential_palette <- function(x) {
  rng <- range(x, na.rm = TRUE)
  if (!all(is.finite(rng))) rng <- c(0, 1)
  if (rng[[1]] == rng[[2]]) rng[[2]] <- rng[[2]] + 1
  leaflet::colorNumeric(
    palette = colorRampPalette(c("#f7fbff", "#c6dbef", "#6baed6", "#2171b5", "#08306b"))(256),
    domain = rng,
    na.color = "#dddddd"
  )
}

