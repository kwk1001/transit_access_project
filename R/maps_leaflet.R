  if (length(default_period) == 0) default_period <- metrics_sf %>% sf::st_drop_geometry() %>% dplyr::slice(1) %>% dplyr::pull(layer_group)

  for (metric_col in period_metrics) {
    if (!metric_col %in% names(metrics_sf)) next
    out_path <- file.path(cfg$paths$maps_dir, paste0(metric_col, ".html"))
    build_metric_map(
      sf_obj = metrics_sf,
      county_outlines = county_outlines,
      metric_col = metric_col,
      group_col = "layer_group",
      popup_cols = c("NAME", "origin_id", "period_id", "time_window_id", "scenario_id", metric_col),
      output_path = out_path,
      default_group = default_period
    )
    index_entries <- bind_rows(index_entries, tibble(file = out_path, title = metric_short_title(metric_col), description = metric_description(metric_col), section = "Accessibility level maps"))
  }

  comparisons_sf <- ensure_origin_id_column(comparisons_sf, cfg$geography$analysis_unit) %>%
    mutate(
      scenario_id = as.character(scenario_id),
      time_window_id = as.character(time_window_id),
      comparison_id = as.character(comparison_id)
    ) %>%
    filter(
      origin_id %in% valid_zone_ids,
      is_valid_zone_id_for_unit(origin_id, cfg$geography$analysis_unit),
      !is.na(scenario_id),
      !is.na(time_window_id),
      !is.na(comparison_id)
    ) %>%
    mutate(layer_group = paste0(time_window_id, " | ", comparison_id, " | ", scenario_id))
  default_comparison_id <- cfg$map$default_comparison_id %||% (comparisons_sf %>% sf::st_drop_geometry() %>% dplyr::slice(1) %>% dplyr::pull(comparison_id))
  default_compare <- comparisons_sf %>% sf::st_drop_geometry() %>% dplyr::filter(comparison_id == default_comparison_id, time_window_id == (cfg$map$default_time_window_id %||% first(time_window_id))) %>% dplyr::slice(1) %>% dplyr::pull(layer_group)
  if (length(default_compare) == 0) default_compare <- comparisons_sf %>% sf::st_drop_geometry() %>% dplyr::slice(1) %>% dplyr::pull(layer_group)

  for (metric_col in comparison_metrics) {
    if (!metric_col %in% names(comparisons_sf)) next
    out_path <- file.path(cfg$paths$maps_dir, paste0(metric_col, ".html"))
    build_metric_map(
      sf_obj = comparisons_sf,
      county_outlines = county_outlines,
      metric_col = metric_col,
      group_col = "layer_group",
      popup_cols = c("NAME", "origin_id", "comparison_id", "time_window_id", "scenario_id", metric_col),
      output_path = out_path,
      default_group = default_compare
    )
    index_entries <- bind_rows(index_entries, tibble(file = out_path, title = metric_short_title(metric_col), description = metric_description(metric_col), section = "Accessibility change maps"))
  }

  origin_sf <- build_od_marginal_sf(tracts_sf, od_origin, "origin_id", "origin_total_weight", unit = cfg$geography$analysis_unit) %>%
    mutate(
      scenario_id = as.character(scenario_id)
    ) %>%
    filter(
      origin_id %in% valid_zone_ids,
      is_valid_zone_id_for_unit(origin_id, cfg$geography$analysis_unit),
      !is.na(scenario_id)
    ) %>%
    mutate(layer_group = scenario_id)
  destination_sf <- build_od_marginal_sf(tracts_sf, od_destination, "destination_id", "destination_total_weight", unit = cfg$geography$analysis_unit) %>%
    mutate(
      scenario_id = as.character(scenario_id)
    ) %>%
    filter(
      destination_id %in% valid_zone_ids,
      is_valid_zone_id_for_unit(destination_id, cfg$geography$analysis_unit),
      !is.na(scenario_id)
    ) %>%
    mutate(layer_group = scenario_id)
  default_od_group <- cfg$map$default_time_window_id %||% (origin_sf %>% sf::st_drop_geometry() %>% dplyr::slice(1) %>% dplyr::pull(layer_group))
  if (!default_od_group %in% unique(origin_sf$layer_group)) default_od_group <- origin_sf %>% sf::st_drop_geometry() %>% dplyr::slice(1) %>% dplyr::pull(layer_group)

  if ("origin_total_weight" %in% names(origin_sf)) {
    out_path <- file.path(cfg$paths$maps_dir, "origin_total_weight.html")
    build_metric_map(origin_sf, county_outlines, "origin_total_weight", "layer_group", c("NAME", "GEOID", "scenario_id", "origin_total_weight"), out_path, default_od_group)
    index_entries <- bind_rows(index_entries, tibble(file = out_path, title = metric_short_title("origin_total_weight"), description = metric_description("origin_total_weight"), section = "OD importance maps"))
  }

  if ("destination_total_weight" %in% names(destination_sf)) {
    out_path <- file.path(cfg$paths$maps_dir, "destination_total_weight.html")
    build_metric_map(destination_sf, county_outlines, "destination_total_weight", "layer_group", c("NAME", "GEOID", "scenario_id", "destination_total_weight"), out_path, default_od_group)
    index_entries <- bind_rows(index_entries, tibble(file = out_path, title = metric_short_title("destination_total_weight"), description = metric_description("destination_total_weight"), section = "OD importance maps"))
  }

  lines_sf <- build_od_lines_sf(od_top_pairs, tract_centroids, analysis_zones, max_pairs = od_top_pairs_max)
  if (!is.null(lines_sf) && nrow(lines_sf) > 0) {
    out_path <- file.path(cfg$paths$maps_dir, "od_top_pairs_interactive.html")
    build_od_line_map(lines_sf, county_outlines, out_path, default_group = default_od_group, default_top_n = od_top_pairs_default)
    index_entries <- bind_rows(index_entries, tibble(file = out_path, title = "Interactive top OD desire lines", description = "Origin and destination centroids are connected for the most important OD pairs in the selected survey scenario. Use the slider to change how many top OD pairs are displayed.", section = "OD importance maps"))
  }

  write_maps_index(index_entries, file.path(cfg$paths$maps_dir, "index.html"), paste0(cfg$project$city_label, " accessibility and OD maps"))
  invisible(index_entries)
}
